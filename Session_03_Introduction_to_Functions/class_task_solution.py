# Class Task Solution: Modular Calculator

def multiply_numbers(a, b):
    return a * b

def divide_numbers(x, y):
    return x / y

# Calling the functions and storing the results
product = multiply_numbers(10, 5)
quotient = divide_numbers(20, 4)

# Printing the results
print("The product of 10 and 5 is:", product)
print("The quotient of 20 and 4 is:", quotient)
